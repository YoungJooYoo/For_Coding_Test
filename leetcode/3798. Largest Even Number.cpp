class Solution 
{
public:
    string largestEven(string& s) const
    {
        assert(MIN_LENGTH <= s.size() && s.size() <= MAX_LENGTH);
        while (!s.empty() && s.back() == '1')
        {
            assert(s.back() == '1' or s.back() == '2');
            s.pop_back();
        }

        return s;
    }

private:
    enum
    {
        MIN_LENGTH = 1,
        MAX_LENGTH = 100
    };
};

