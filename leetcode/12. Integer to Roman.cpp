class Solution 
{
public:
    string intToRoman(int num)
    {
        assert(num >= MIN_NUM && num <= MAX_NUM);
        string result;
        
        for (const auto &entry : mRomanMapping) // 내림차순으로 정렬된 로마 숫자 룩업 테이블을 이용해 변환
        {
            const int value = entry.first;
            const string& symbol = entry.second;
            while (num >= value)
            {
                result += symbol;
                num -= value;
            }
        }

        assert(result.size() > 0);
        return result;
    }
    
private:
    enum
    {
        ARRAY_SIZE = 13,
        MIN_NUM = 1,
        MAX_NUM = 3999
    };

    const array<pair<int, string>, ARRAY_SIZE> mRomanMapping = 
    {
        {
            {1000, "M"},
            {900, "CM"},
            {500, "D"},
            {400, "CD"},
            {100, "C"},
            {90, "XC"},
            {50, "L"},
            {40, "XL"},
            {10, "X"},
            {9, "IX"},
            {5, "V"},
            {4, "IV"},
            {1, "I"}
        }
    };
};

