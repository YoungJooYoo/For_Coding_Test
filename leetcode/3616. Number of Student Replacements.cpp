class Solution
{
public:
    int totalReplacements(vector<int>& ranks) const
    {
        const size_t LENGTH = ranks.size();
        int replaceCount = 0;
   
        assert(MIN_LENGTH <= LENGTH && LENGTH <= MAX_LENGTH);

        for (size_t i = 1; i < LENGTH; ++i)
        {
            assert(MIN_RANK <= ranks[i] && ranks[i] <= MAX_RANK);
            if (ranks[0] > ranks[i])
            {
                swap(ranks[0], ranks[i]);
                ++replaceCount;
            }
        }

        return replaceCount;
    }

private:
    enum
    {
        MIN_LENGTH = 1,
        MAX_LENGTH = 100000,   // ë¬¸ì œ ì¡°ê±´: 1 <= ranks.length <= 1e5
        MIN_RANK   = 1,
        MAX_RANK   = 100000    // ë¬¸ì œ ì¡°ê±´: 1 <= ranks[i] <= 1e5
    };
};

