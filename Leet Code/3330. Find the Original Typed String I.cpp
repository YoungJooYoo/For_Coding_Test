class Solution 
{
public:
    int possibleStringCount(const string& word) 
    {
        const size_t LENGTH = word.size();
        int count = 1;

        for (size_t i = 0; i < LENGTH - 1; ++i)
        {
            if (word[i] == word[i + 1])
            {
                ++count;
            }
        }

        return count;
    }
};


